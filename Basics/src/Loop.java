public class Loop {
  public static void main(String[]args){

    //initalization, condition, updation

    // for loop 
    for(int i=1; i<11; i++){
      System.out.println(i);
    }

    // while loop
    int j = 1;
    while(j<11){
      System.out.println("Hello World");
      j++;
    }

    // do while loop
    int k = 12;
    do{
      System.out.println("bye world");
      k++;
    

    }while(k<11);

  }
}
